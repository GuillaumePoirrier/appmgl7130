package com.mgl7130.curve.pages.student.ui.profile.models;

public class StudentProfileFields {

    private String studentFirstName;
    private String studentFamilyName;
    private String studentBirthDate;
    private String studentDescription;

    public String getStudentFirstName() { return studentFirstName; }

    public void setStudentFirstName(String studentFirstName) { this.studentFirstName = studentFirstName; }

    public String getStudentFamilyName() { return studentFamilyName; }

    public void setStudentFamilyName(String studentFamilyName) { this.studentFamilyName = studentFamilyName; }

    public String getStudentBirthDate() { return studentBirthDate; }

    public void setStudentBirthDate(String studentBirthDate) { this.studentBirthDate = studentBirthDate; }

    public String getStudentDescription() { return studentDescription; }

    public void setStudentDescription(String studentDescription) { this.studentDescription = studentDescription; }
}
