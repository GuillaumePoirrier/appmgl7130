package com.mgl7130.curve.pages.teacher.ui.profile_create.models;

public class ProfileFields {

    private String teacherFirstName;
    private String teacherFamilyName;
    private String teacherBirthDate;
    private String teacherDescription;

    public String getTeacherFirstName() {
        return this.teacherFirstName;
    }

    public void setTeacherFirstName(String teacherFirstName) { this.teacherFirstName = teacherFirstName; }

    public String getTeacherFamilyName() {
        return this.teacherFamilyName;
    }

    public void setTeacherFamilyName(String teacherFamilyName) { this.teacherFamilyName = teacherFamilyName; }

    public String getTeacherBirthDate() {
        return this.teacherBirthDate;
    }

    public void setTeacherBirthDate(String teacherBirthDate) { this.teacherBirthDate = teacherBirthDate; }

    public String getTeacherDescription() {
        return this.teacherDescription;
    }

    public void setTeacherDescription(String teacherDescription) { this.teacherDescription = teacherDescription; }
}